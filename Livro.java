package Package;

public class Livro {
	int id, ano;
	String titulo, autor, editora;
	
	public Livro(int id, int ano, String titulo, String autor, String editora) {
		this.id = id;
		this.ano = ano;
		this.titulo = titulo;
		this.autor = autor;
		this.editora = editora;
	}
	
	public String consultarLivro() {
		
		return titulo;
	}

}
